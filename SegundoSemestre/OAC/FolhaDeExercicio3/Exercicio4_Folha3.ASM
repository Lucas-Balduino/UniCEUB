SOMA:	ADD	AL,02	;SOMO AL + 2
	MOV	[DL],AL	;ARMAZENO NO ENDEREÇO INDICADO EM DL
	INC	DL	;INCREMENTO DL PARA APONTAR PARA O PRÓXIMO
	CMP	DL,70	;SE DL FOR IGUAL A 70, Z=1 E ENCERRRA O LAÇO
	JNZ	SOMA	;SE DL FOR MENOR QUE 70, Z=0 E PASSA PARA A PROXIMA ITERAÇÃO
	
	MOV	BL,60	;VALOR INICIAL DO END
PILHA:	MOV	CL,[BL]	;ARMAZENO NO ENDEREÇO INDICADO EM DL
	PUSH	CL	;EMPILHO
	INC	BL	;INCREMENTO DL PARA APONTAR PARA O PRÓXIMO
	CMP	BL,70	;SE DL FOR IGUAL A 70, Z=1 E ENCERRRA O LAÇO
	JNZ	PILHA	;SE DL FOR MENOR QUE 70, Z=0 E PASSA PARA A PROXIMA ITERAÇÃO

	
	
