;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;RODA O MOTOR NO SENTIDO ANTIHORARIO;
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

	
	MOV	BL,10	;INICIO CONTADOR, VAI RODAR 16 VEZES
	MOV	AL,88	;VALOR INICIAL 88 = 1000 1000
RODA:	OUT	05	;ENVIO VALOR DE AL PARA MOTOR DE PASSO
	ROR	AL	;RODO AL1 BIT A DIREITA
	DEC	BL	;DECREMENTO O CONTADOR
	JNZ	RODA	;SE BL = 0, Z=1 E PARO, BL > 0, Z=0 E PULO PARA RODA

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;RODA O MOTOR NO SENTIDO HORARIO;
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

	MOV	BL,10	;INICIO CONTADOR, VAI RODAR 16 VEZES
	MOV	AL,88	;VALOR INICIAL 88 = 1000 1000
RODA2:	OUT	05	;ENVIO VALOR DE AL PARA MOTOR DE PASSO
	ROl	AL	;RODO AL1 BIT A ESQUERDA
	DEC	BL	;DECREMENTO O CONTADOR
	JNZ	RODA2	;SE BL = 0, Z=1 E PARO, BL > 0, Z=0 E PULO PARA RODA

	END